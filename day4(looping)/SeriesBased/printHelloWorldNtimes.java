package SeriesBased;
import java.util.Scanner;
public class printHelloWorldNtimes {
      public static void main(String []args)
    {
        Scanner s=new Scanner(System.in);
        int n=s.nextInt();
        for(int i=0;i<n;i++)
        {
            
            {
                System.out.print("Hello World\n");
            }
        }
    }
}
